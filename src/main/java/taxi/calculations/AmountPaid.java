package taxi.calculations;

public class AmountPaid {
    double amountPaid;
    double price;

    public void setAmountPaid(double amountPaid) {
        this.amountPaid = amountPaid;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getPrice() {
        return price;
    }

    public double getAmountPaid() {
        return amountPaid;
    }
}
