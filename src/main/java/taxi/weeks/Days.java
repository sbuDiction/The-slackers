package taxi.weeks;

public class Days {
    int id;
    String daysInAWeek;

    public void setDaysInAWeek(String daysInAWeek) {
        this.daysInAWeek = daysInAWeek;
    }

    public String getDaysInAWeek() {
        return daysInAWeek;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }
}
